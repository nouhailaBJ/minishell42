#include "../minishell.h"

void	ft_pwd(void)
{
	char	cwd[MAX_PATH];

	getcwd(cwd, MAX_PATH);
	ft_putstr_fd(cwd, 1);
	write(1, "\n", 1);
	g_data.status = 0;
}
